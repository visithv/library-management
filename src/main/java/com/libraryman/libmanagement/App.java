    private void renderLogs()
    {
        DefaultTableModel model = (DefaultTableModel) logs_table.getModel();
        clearTable(model);
        
        List<Log> logs = Database.getLogs();
        
        for (int i = 0; i < logs.size(); i ++)
        {
            Log l = logs.get(i);            
            Object[] values = { l.bookid, l.name, l.datetaken.toString(), l.returndate.toString() };
            model.addRow(values);
        }
    }
    
    private void renderInventory()
    {
        DefaultTableModel model = (DefaultTableModel) inventory_table.getModel();
        clearTable(model);
        
        List<Book> books = Database.getBooks(
            book_id_txt.getText(),
            book_title_txt.getText(), 
            author_name_txt.getText(), 
            before_year.getValue(),
            after_year.getValue()
        );
        
        for (int i = 0; i < books.size(); i++)
        {
            Book b = books.get(i);
            Object[] values = { b.bookId.toString(), b.title, b.author, b.description, String.valueOf(b.published), b.availability };
            model.addRow(values);
        }
    }
    
    private void clearTable(DefaultTableModel model)
    {
        int rowCount = model.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }
        

