    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int[] selected = inventory_table.getSelectedRows();
        
        if (selected.length > 0)
        {
            // edit book details
            for (int i = 0; i < selected.length; i++)
            {
                // get first column of inventory table and open a dialog to edit book details
                Object bid = (inventory_table.getValueAt(selected[i], 0));
                
                EditBookDialog edit = new EditBookDialog();
                edit.bookId = Integer.parseInt(bid.toString());
                edit.updateDetails();
                edit.setLocationRelativeTo(null);
                edit.setVisible(true);
                edit.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select at least one book to edit details of.", "Editing book details", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

