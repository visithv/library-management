    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int[] selected = inventory_table.getSelectedRows();
        
        if (selected.length > 0)
        {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog (null, "Are you sure you want to delete the selected books from your inventory?","Warning", dialogButton);
            
            if(dialogResult == JOptionPane.YES_OPTION) {
                // delete book options
                for (int i = 0; i < selected.length; i++)
                {
                    // get first column of inventory table and open a dialog to edit book details
                    int bid = Integer.parseInt(inventory_table.getValueAt(selected[i], 0).toString());
                    Database.deleteBook(bid);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select at least one book to delete", "Deleting book(s)", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

